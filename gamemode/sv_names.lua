function GetRandomPlayerNameAndSurname()

	if table.IsEmpty(PlayerNames) or table.IsEmpty(PlayerSurnames) then

		return "Shur", "Ambo"
	end

	local NameIndex, SurnameIndex = math.random(#PlayerNames), math.random(#PlayerSurnames)

	local OutName, OutSurname = PlayerNames[NameIndex], PlayerSurnames[SurnameIndex]

	table.remove(PlayerNames, NameIndex)

	table.remove(PlayerSurnames, SurnameIndex)

	return OutName, OutSurname
end